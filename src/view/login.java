    public void validar() {
        String usuario = txtUsuario.getText();
        String password = String.valueOf(txtPass.getPassword());
        //validacion si estan vacios los campos.
        if (!"".equals(usuario) || !"".equals(password)) {
            Login lg = new Login();
            LoginDao loginDao = new LoginDao();

            lg = loginDao.log(usuario, password);
            if (lg.getUsuario() != null && lg.getPassword() != null) {
                JOptionPane.showMessageDialog(null, "Bienvenid@ " + usuario + "!");
                Sistema_Ventas sistema = new Sistema_Ventas(lg);
                sistema.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "El Username o el Password Son Incorrectos, Intentelo de Nuevo");
                txtUsuario.requestFocus();
            }
        }
    }

    public String username() {
        String usuario = txtUsuario.getText();
        return usuario;
    }

